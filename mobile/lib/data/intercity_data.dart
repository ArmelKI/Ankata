  /// Rechercher des routes entre deux villes
  static List<InterCityRoute> searchRoutes(String from, String to) {
    final fromLower = from.toLowerCase();
    final toLower = to.toLowerCase();
    return routes
        .where((r) =>
            r.villeDepart.toLowerCase().contains(fromLower) &&
            r.villeArrivee.toLowerCase().contains(toLower))
        .toList();
  }

  /// Routes d'une compagnie
  static List<InterCityRoute> getRoutesByCompany(String company) {
    return routes
        .where((r) => r.compagnie.toLowerCase().contains(company.toLowerCase()))
        .toList();
  }

  /// Toutes les villes de départ disponibles
  static List<String> getDepartureCities() {
    return routes.map((r) => r.villeDepart).toSet().toList()..sort();
  }

  /// Toutes les villes d'arrivée depuis une ville donnée
  static List<String> getDestinationsFrom(String city) {
    return routes
        .where((r) => r.villeDepart.toLowerCase() == city.toLowerCase())
        .map((r) => r.villeArrivee)
        .toSet()
        .toList()
      ..sort();
  }

  /// Compagnies desservant un trajet
  static List<String> getCompaniesForRoute(String from, String to) {
    return searchRoutes(from, to).map((r) => r.compagnie).toSet().toList();
  }
