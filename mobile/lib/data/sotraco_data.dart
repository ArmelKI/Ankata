import 'package:latlong2/latlong.dart';

class SotracoStop {
  final int ordre;
  final String nom;
  final double lat;
  final double lng;
  final String premierDepart;
  final String dernierDepart;
  final String frequence;

  const SotracoStop({
    required this.ordre,
    required this.nom,
    required this.lat,
    required this.lng,
    required this.premierDepart,
    required this.dernierDepart,
    required this.frequence,
  });

  LatLng get position => LatLng(lat, lng);
}

class SotracoLine {
  final String id;
  final String code;
  final String name;
  final String ville;
  final String type; // Ordinaire, Intercommunale, Spéciale Étudiants
  final List<SotracoStop> stops;

  const SotracoLine({
    required this.id,
    required this.code,
    required this.name,
    required this.ville,
    required this.type,
    required this.stops,
  });

  List<LatLng> get coordinates => stops.map((s) => s.position).toList();
  int get stopsCount => stops.length;
  String get firstDeparture => stops.isNotEmpty ? stops.first.premierDepart : '';
  String get lastDeparture => stops.isNotEmpty ? stops.first.dernierDepart : '';
  String get frequency => stops.isNotEmpty ? stops.first.frequence : '';
}

